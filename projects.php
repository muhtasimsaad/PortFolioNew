                        <h3>Sudoku Solver</h3>

                        <p>The .NET application can solver any Sudoku withing milliseconds. Though  The exact time depends on the difficulty level of the riddle, it is usually withing half a seconds for even the hardest ones.</p>
                        
                        <p>
                        First the application tries to solve the riddle using simple logic, eliminating possibilities from
                        each box using row, column and 3/3 grid rules. The grids that are solved using this simple logics 
                        are marked green after the riddle is solved.</p>
                        
                        <p>
                        However in grids that command a higher difficulty level, using Logic does not help much. At this point, the application uses DFS algorith to start traversing possibilities one by one ubtil it finds the solution. This can lead to a very complex situations as there can be up to 9 possibilities for each block, therefore the programm tries to find a soft spot with the minimum number of possibilities and start guessing from that point using DFS algorithm.
                        </p>
                        
                         <p>
                        At the end the <span style="color: green;font-style: bold"><strong>green</strong></span> colored boxes are the ones that were solved using simple logic and with no help from DFS algorithm. The <span style="color: purple;font-style: bold"><strong>purple</strong></span> blocks are the ones provided in the riddle. The <span style="color: cyan;font-style: bold"><strong>cyan</strong></span> blocks are the ones that were solved using DFS algorithm. So, with a higher degree of difficulty there are likely to be more <span style="color: cyan;font-style: bold"><strong>cyan</strong></span> boxes than <span style="color: purple;font-style: bold"><strong>purple</strong></span> ones in the solution.</p>

                         
                       
